//        {
//            builder.WithOrigins("http://192.168.1.7:5220")
//                   .AllowAnyHeader()
//                   .AllowAnyMethod()
//                   .AllowAnyOrigin();
//        });
//});
var app = builder.Build();
app.UseSerilogRequestLogging();
// Configure the HTTP request pipeline.
app.UseSwagger();
app.UseSwaggerUI();

app.UseHttpsRedirection();

app.UseAuthentication();

app.UseAuthorization();

app.MapControllers();

app.Run();
